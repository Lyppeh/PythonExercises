n = input('Digite algo')
print('o tipo primitivo desse valor é' , type(n))
print('É considerado um titulo?', n.istitle())
print('É considerado um Numero?', n.isnumeric())
print('É considerado um alfanumerico', n.isalpha())
print('É composto somente de letras?', n.isalpha())
print('É composto somente de espaços?', n.isspace())
print('É composto somente de letras maiúsculas?', n.isupper())
print('É composto somente de letras minúsculas?', n.islower())













